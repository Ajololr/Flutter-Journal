class Student {
  String firstName;
  String lastName;
  String secondName;
  List<String> images;
  DateTime birthday;
  String lattitude;
  String longitude;
  String videoUrl;

  Student(String firstName, String lastName, String secondName, List<String> images, DateTime birthday, String lattitude, String longitude, String videoUrl) {
    this.firstName = firstName;
    this.firstName = firstName;
    this.lastName = lastName;
    this.secondName = secondName;
    this.images = images;
    this.birthday = birthday;
    this.lattitude = lattitude;
    this.longitude = longitude;
    this.videoUrl = videoUrl;
  }
}
